package Java;

public class CastingDiscussion {

	// Casting==process of conversion of one type of data into anather type of data

	// Types==Primitive type casting==implicit casting, explicit casting
	// Non primitive type casting==Up casting, down casting

	public static void main(String[] args) {

		int a = 90;// lower order data type

		double d = (double) a;// higher order data type

		System.out.println(d);

	}

}
