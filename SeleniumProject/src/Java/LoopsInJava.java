package Java;

public class LoopsInJava {

	// Loops==Whenever we want to execute any logic or set of code in repeated
	// manner
	// at that time we will use loop

	// Types==do while loop
	// while loop
	// for loop
	public static void main(String[] args) {

		int a = 0;

		while (a < 10) {
			System.out.println(a);
			a++;

		}

	}
}
